void ST7789_init(char pins)
{
    int i;

    _STCLK = pins;
    _STMISO = pins+1;
    _STMOSI = pins+2;
    _STCS = pins+3;
    _STRESET = pins+4;
    _STDC = pins+5;
    _STCD = pins+6;
    _STLITE = pins+7;

    spi = Spi_Init(_STMOSI, _STMISO, _STCLK, 0, 255);

    _pinl(_STRESET);
    _waitms(50);
    _pinh(_STRESET);
    _waitms(50);

    _pinl(_STCS);
    _pinh(_STLITE);

    writeCmd(ST7789_SWRESET);
    _waitms(150); // 150
    writeCmd(ST7789_SLPOUT);
    _waitms(10); // 10
    writeCmd(ST7789_COLMOD);
    spi_out(8, 0x55);
    _waitms(10); // 10
    writeCmd(ST7789_INVON);
    _waitms(10); // 10
    writeCmd(ST7789_NORON);
    _waitms(10); // 10
    writeCmd(ST7789_DISPON);
    _waitms(10); // 10
    writeCmd(ST7789_DISBV);
    spi_out(16, 0xff);

    ST7789_Rotation(1);

    _pinh(_STCS);

    _XCursor = 0;
    _YCursor = 0;
    _BColor = ST7789_BLACK;
    _FColor = ST7789_WHITE;
}

void ST7789_Rotation(char rotation)
{
  unsigned char madctl;

  switch (rotation)
  {
  case 0: madctl = 0; // ST7789_MADCTL_MX | ST7789_MADCTL_MY;
    _XStart = 53;
    _YStart = 40;
    _STWidth = ST7789_WIDTH;
    _STHeight = ST7789_HEIGHT;
    break;
  case 1: madctl = ST7789_MADCTL_MX | ST7789_MADCTL_MV;
    _XStart = 40;
    _YStart = 53;
    _STWidth = ST7789_HEIGHT;
    _STHeight = ST7789_WIDTH;
    break;
  case 2: madctl = ST7789_MADCTL_MX | ST7789_MADCTL_MY;
    _XStart = 53;
    _YStart = 40;
    _STWidth = ST7789_WIDTH;
    _STHeight = ST7789_HEIGHT;
    break;
  case 3: madctl = ST7789_MADCTL_MY | ST7789_MADCTL_MV;
    _XStart = 40;
    _YStart = 52;
    _STWidth = ST7789_HEIGHT;
    _STHeight = ST7789_WIDTH;
    break;
  default:
    break;
  }

    writeCmd(ST7789_MADCTL);
    spi_out(8, madctl);
}

void ST7789_Cls(int color)
{
    _BColor = color;
    ST7789_FillRectangle(0, 0, _STWidth, _STHeight, color);
}

void ST7789_Window(int sx, int sy, int w, int h)
{
    int x, y;

    x = (sx + _XStart) << 16 | (sx + _XStart + w - 1);
    y = (sy + _YStart) << 16 | (sy + _YStart + h - 1);

    writeCmd(ST7789_CASET);
    spi_out(32, x);

    writeCmd(ST7789_RASET);
    spi_out(32, y);

    writeCmd(ST7789_RAMWR);

}

void ST7789_FillRectangle(int sx, int sy, int width, int height, int color)
{
    ST7789_Window(sx, sy, width, height);
    ST7789_WriteColor(color, width * height);
}

void ST7789_WriteColor(int color, int size)
{
    for (int i=0;i<size;i++)
        spi_out(16, color);
}

void ST7789_Plot(int x, int y, int color)
{

    if ((x < 0) || (y < 0) || (x >= _STWidth) || (y >= _STHeight))
        return;

    ST7789_Window(x, y, 1, 1);
    spi_out(16, color);
}

void ST7789_Display(int state)
{
    if (state == 0)
        writeCmd(ST7789_DISPOFF);
    else
        writeCmd(ST7789_DISPON);
}

void ST7789_Sleep(int state)
{
    if (state == 0)
        writeCmd(ST7789_SLPOUT);
    else
        writeCmd(ST7789_SLPIN);
    _waitms(120);
}

