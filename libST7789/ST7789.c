void ST7789_WriteSCharacter(char character)
{
    int t;
    long v;

    ST7789_Window(_XCursor, _YCursor, 8, 8);

    t = character * 2;
    for (int l=0;l<2;l++)
    {
        v = Font_57[t++];
        for (int i=0;i<4;i++)
        {
            for (int j=0;j<8;j++)
            {
                if ((v & 0x01) == 1)
                    spi_out(16, _FColor);
                else
                    spi_out(16, _BColor);
                v = v >> 1;
            }
        }
    }

    _XCursor = _XCursor + 8;
    if (_XCursor > _STWidth)
    {
        _XCursor = 0;
        _YCursor = _YCursor + 8;
    }
}

void ST7789_WriteCharacter(char character)
{
    long *base;
    int offset;
    long v;

    ST7789_Window(_XCursor, _YCursor, 16, 32);

    offset = character & 0xfe; // two characters per location
    base = &PropellerFont[offset*16]; // jump to character

    offset = 0;
    for (int i=0;i<32;i++)
    {
        v = base[offset++];
        if (character & 0x01)
            v = v >> 1;
        
        for (int j=0;j<16;j++)
        {
            if ((v & 0x01) == 1)
                spi_out(16, _FColor);
            else
                spi_out(16, _BColor);
            v = v >> 2;
        }
    }

    _XCursor = _XCursor + 16;
    if (_XCursor > _STWidth)
    {
        _XCursor = 0;
        _YCursor = _YCursor + 32;
    }
}

void ST7789_WriteString(char *string)
{
    int i = 0;

    while (string[i] > 0)
        ST7789_WriteCharacter(string[i++]);
}

void ST7789_WriteXCharacter(char character)
{
    long *base;
    int offset;
    long v;

    ST7789_Window(_XCursor, _YCursor, 32, 64);

    offset = character & 0xfe; // two characters per location
    base = &PropellerFont[offset*16]; // jump to character

    offset = 0;
    for (int i=0;i<64;i++)
    {
        if ((i & 0x01) == 1)
            v = base[offset++];
        else
            v = base[offset];
        
        if (character & 0x01)
            v = v >> 1;
        
        for (int j=0;j<16;j++)
        {
            if ((v & 0x01) == 1)
            {
                spi_out(16, _FColor);
                spi_out(16, _FColor);
            }
            else
            {
                spi_out(16, _BColor);
                spi_out(16, _BColor);
            }
            v = v >> 2;
        }
    }

    _XCursor = _XCursor + 32;
    if (_XCursor > _STWidth)
    {
        _XCursor = 0;
        _YCursor = _YCursor + 64;
    }
}

void ST7789_WriteXString(char *string)
{
    int i = 0;

    while (string[i] > 0)
        ST7789_WriteXCharacter(string[i++]);
}

void ST7789_Drawline(int x, int y, int ex, int ey, int color)
{
    short dx, dy, D, xc, yc, z;
  
    dx = ex - x;
    if (dx < 0)
    {
        xc = x;x = ex;ex = xc;
        yc = y;y = ey;ey = yc;
    }
    if (dx < 0)
        dx = -dx;
    dy = ey - y;
    if (dy < 0)
        z = -1;
    else
        z = 1;
    if (dy < 0)
        dy = -dy;
    if (dx < dy)
    {
        drawLine(x, y, ex, ey, color);
        return;
    }
    D = 2 * dy - dx;
    yc = y;
  
  for (xc = x;xc <= ex;xc++)
  {
    ST7789_Plot(xc, yc, color);
    if (D > 0)
    {
      yc = yc + z;
      D = D - 2 * dx;
    }
    D = D + 2 * dy;
  }          
}

void drawLine(int x, int y, int ex, int ey, int color)
{
    short dx, dy, D, xc, yc, z;

    dy = ey - y;
    if (dy < 0)
    {
        yc = y;y = ey;ey = yc;
        xc = x;x = ex;ex = xc;
    }
    if (dy < 0)
        dy = -dy;
    dx = ex - x;
    if (dx < 0)
        z = -1;
    else
        z = 1;
    if (dx < 0)
        dx = -dx;
    D = 2 * dx - dy;
    xc = x;

    for (yc = y;yc <= y;y++)
    {
        ST7789_Plot(xc, yc, color);
        if (D > 0)
        {
            xc = xc + z;
            D = D - 2 * dy;
        }
        D = D + 2 * dx;
    }
}

void ST7789_DrawBox(int x, int y, int ex, int ey, int color)
{
    ST7789_Drawline(x, y, ex, y, color);
    ST7789_Drawline(x, y, x, ey, color);
    ST7789_Drawline(ex, y, ex, ey, color);
    ST7789_Drawline(x, ey, ex, ey, color);
}

int ST7789_BuildColor(int red, int green, int blue)
{
    unsigned short c;

    c = red & 0x1f;
    c = (c << 6) | (green & 0x2f);
    c = (c << 5) | (blue & 0x1f);

    return c;
}


