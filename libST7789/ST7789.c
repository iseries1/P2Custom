void writeCmd(unsigned char cmd)
{
  _pinl(_STDC);
  spi_out(8, cmd);
  _pinh(_STDC);
}

void spi_out(unsigned char bits, unsigned int value)
{
    _pinl(_STCS);
    Spi_Out(spi, bits, value);
    //shift_out(_STMOSI, _STCLK, MSBFIRST, bits, value);
    _pinh(_STCS);
}

