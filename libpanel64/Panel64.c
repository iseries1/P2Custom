void doRefresh(void *par)
{
    //setup pins
    _dirh(PNA);
    _dirh(PNB);
    _dirh(PNC);
    _dirh(PND);
    _dirh(PNE);
    _dirh(PNR1);
    _dirh(PNG1);
    _dirh(PNB1);
    _dirh(PNR2);
    _dirh(PNG2);
    _dirh(PNB2);
    _dirh(PNCLK);
    _dirh(PNOE);
    _dirh(PNLAT);
    _pinh(PNOE);
    _pinl(PNLAT);
    _pinh(PNCLK);

    while (1)
    {
        for (int i=0;i<32;i++)
        {
            for (int j=0;j<64;j++)
            {
                doColor(Buffer[j][i], Buffer[j][i+32]);
                _pinl(PNCLK);
                _pinh(PNCLK);
            }
            doAddress(i);
            _pinh(PNLAT);
            _pinl(PNLAT);
            _pinl(PNOE);
            usleep(20);
            _pinh(PNOE);
        }
	}
}

void doColor(int c1, int c2)
{
    if (c1 & 1)
        _pinh(PNR1);
    else
        _pinl(PNR1);

    if (c1 & 2)
        _pinh(PNG1);
    else
        _pinl(PNG1);

    if (c1 & 4)
        _pinh(PNB1);
    else
        _pinl(PNB1);

    if (c2 & 1)
        _pinh(PNR2);
    else
        _pinl(PNR2);

    if (c2 & 2)
        _pinh(PNG2);
    else
        _pinl(PNG2);

    if (c2 & 4)
        _pinh(PNB2);
    else
        _pinl(PNB2);

}

void doAddress(int a)
{
    if (a & 1)
        _pinh(PNA);
    else
        _pinl(PNA);

    if (a & 2)
        _pinh(PNB);
    else
        _pinl(PNB);

    if (a & 4)
        _pinh(PNC);
    else
        _pinl(PNC);

    if (a & 8)
        _pinh(PND);
    else
        _pinl(PND);

    if (a & 16)
        _pinh(PNE);
    else
        _pinl(PNE);
}
