void Panel_DrawLine(int x1, int y1, int x2, int y2, int color)
{
  int dx, dy, D, x, y, z;

  dx = x2 - x1;
  if (dx < 0)
    {
      x = x1; x1 = x2; x2 = x;
      y = y1; y1 = y2; y2 = y;
      dx = -dx;
    }

    z = 1;
    dy = y2 - y1;
    if (dy < 0)
    {
      z = -1;
      dy = -dy;
    }

    if (dx < dy)
    {
      DrawLine(x1, y1, x2, y2, color);
      return;
    }

    D = 2 * dy - dx;
    y = y1;

    for (x = x1; x <= x2; x++)
    {
      Panel_SetPixel(x, y, color);
      if (D > 0)
      {
        y = y + z;
        D = D - 2 * dx;
      }
      D = D + 2 * dy;
    }
}

void DrawLine(int x1, int y1, int x2, int y2, int color)
{
  int dx, dy, D, x, y, z;

  dy = y2 - y1;
  if (dy < 0)
  {
    y = y1; y1 = y2; y2 = y;
    x = x1; x1 = x2; x2 = x;
    dy = -dy;
  }

  z = 1;
  dx = x2 - x1;
  if (dx < 0)
  {
    z = -1;
    dx = -dx;
  }

  D = 2 * dx - dy;
  x = x1;

  for (y = y1; y <= y2; y++)
  {
    Panel_Plot(x, y, color);
    if (D > 0)
    {
      x = x + z;
      D = D - 2 * dy;
    }
    D = D + 2 * dx;
  }
}

